# double quotes
"hello"

# single quotes
'hello'

# escape character
'he\'s a good man'

# get single character
greet = 'hello'
greet[0]  # 'h'
greet[-1]  # 'o'

# slice a string
greet[0:3]  # hel

# concatnation
'hello' + ' ' + 'world'

# repeat a string
greet * 3 # 'hellohellohello'

# to uppercase
greet.upper() # 'HELLO'

# split strings
cheeses = 'brie, chedder, stilton'
cheeses.split(',') # ['brie', 'chedder', 'stilton']

# get length of string
len(greet) # 5

